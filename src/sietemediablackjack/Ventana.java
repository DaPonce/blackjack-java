    private void blackJack(Jugador j){
        taResultado.setText("Se ha producido un Blackjack,\n el ganador es: " + j.getNombre());
        bNuevaPartida.setEnabled(true);
        bPideCarta.setEnabled(false);
        bPlantarse.setEnabled(false);
    }
    
    public void mNuevaPartida(){
        jugadasSeguidas = 0;
        puntuacionBanca = 0;
        puntuacion = 0;
        jlPuntuacion.setText(null);
        jlPuntuacionBanca.setText(null);
        TiposDeBaraja barajaSeleccionada = TiposDeBaraja.devuelveBaraja((String) comboBaraja.getSelectedItem());
        if(barajaSeleccionada.getNombre().equals(TiposDeBaraja.BARAJA1.getNombre())){
            this.setTitle(TiposDeBaraja.BARAJA1.getNombre());
            laPartidaActual = new Partida(new Jugador(nombre), new Jugador("Banca"), TiposDeBaraja.BARAJA1);
            valorBanca = 5.0f;
        } else if(barajaSeleccionada.getNombre().equals(TiposDeBaraja.BARAJA2.getNombre())){
            this.setTitle(TiposDeBaraja.BARAJA2.getNombre());
            laPartidaActual = new Partida(new JugadorBlackJack(nombre), new JugadorBlackJack("Banca"), TiposDeBaraja.BARAJA2);
            valorBanca = 16.0f;
        }
        this.setTitle(nombre + " " + barajaSeleccionada.getNombre());
        bNuevaPartida.setEnabled(false);
        bPideCarta.setEnabled(true);
        bPlantarse.setEnabled(false);
        
        jpCartasBanca.removeAll();
        jpCartasJugador.removeAll();
        jpCartasBanca.repaint();
        jpCartasJugador.repaint();
        taResultado.setText("");
        gestora = new GestoraDeBarajas(barajaSeleccionada);
    }
    
