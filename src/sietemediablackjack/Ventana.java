    private void bPideCartaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPideCartaActionPerformed
        // TODO add your handling code here:
        Carta nuevaCarta = laPartidaActual.obtenerOtraCarta(gestora);
        laPartidaActual.jugador.add(nuevaCarta);
        JLabel etiqueta = new JLabel();
        jpCartasJugador.add(etiqueta);
        ImageIcon imagenCarta = new ImageIcon(getClass().getResource("/sietemediablackjack/" + nuevaCarta.getImagen()));
        etiqueta.setIcon(imagenCarta);
        puntuacion += nuevaCarta.getValor();
        jlPuntuacion.setText("Jugador: " + String.valueOf(puntuacion) + " puntos.");
        jugadasSeguidas++;
        bPlantarse.setEnabled(true);
        if(laPartidaActual.jugador.devuelveSumaCartas() == 21.0f && jugadasSeguidas == 2){
            blackJack(laPartidaActual.jugador);
        }
        if(laPartidaActual.jugador.devuelveSumaCartas() > laPartidaActual.getLimite()){
            mPlantarse();
        }
    }//GEN-LAST:event_bPideCartaActionPerformed

    private void mPlantarse(){
        jugadasSeguidas = 0;
        JOptionPane.showMessageDialog(this, "Es el turno de la banca.");
        bNuevaPartida.setEnabled(true);
        bPideCarta.setEnabled(false);
        bPlantarse.setEnabled(false);
        mGenerarCartasBanca();
    }
    
    private void mGenerarCartasBanca(){
        while(laPartidaActual.banca.devuelveSumaCartas() <= valorBanca){
            JOptionPane.showMessageDialog(this, "La banca pide una carta.");
            Carta nuevaCarta = laPartidaActual.obtenerOtraCarta(gestora);
            laPartidaActual.banca.add(nuevaCarta);
            JLabel etiqueta = new JLabel();
            jpCartasBanca.add(etiqueta);
            ImageIcon imagenCarta = new ImageIcon(getClass().getResource("/sietemediablackjack/" + nuevaCarta.getImagen()));
            etiqueta.setIcon(imagenCarta);
            puntuacionBanca += nuevaCarta.getValor();
            jlPuntuacionBanca.setText("Banca: " + String.valueOf(puntuacionBanca) + " puntos.");
            jugadasSeguidas++;
            if(laPartidaActual.banca.devuelveSumaCartas() > laPartidaActual.getLimite()){
                break;
            }
        }
        if(laPartidaActual.banca.devuelveSumaCartas() == 21 && jugadasSeguidas == 2){
                blackJack(laPartidaActual.banca);
        } else {
            JOptionPane.showMessageDialog(this, "La banca se planta.");
            darResultados();   
        }
    }
    
